import fs from 'fs';
import { ClientManifest } from '../manifest';
export class VersionInfo {
    static CreateVersionFileFromManifest(path: string, manifest: ClientManifest): void {
        var fullText = "";
        fullText += "// DO NOT EDIT; AUTOGENERATED FILE BY OSWUPDATER\n"
        fullText += "namespace OpenSteamworks.Generated;\n"
        fullText += "\n";
        fullText += "public static class VersionInfo {\n"
        fullText += `   public const uint STEAM_MANIFEST_VERSION = ${manifest.version};\n`
        fullText += "}\n"
        fs.writeFileSync(path, fullText);
    }
}